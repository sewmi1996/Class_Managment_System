    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        if(txtuname.getText().equals("")){
           JOptionPane.showMessageDialog(null,"Please enter Username ");
           txtuname.setBorder(BorderFactory.createLineBorder(Color.decode("#FF0000")));
            txtuname.grabFocus();
            return;
        }
       else if(new String(txtpassword.getPassword()).equals("")){
           JOptionPane.showMessageDialog(null,"Please enter Password ");
           txtpassword.setBorder(BorderFactory.createLineBorder(Color.decode("#FF0000")));
           txtpassword.grabFocus();
           return;
        }
    try {
        ResultSet rs=jdbc.getData("SELECT Username,Position FROM user_details WHERE Password=password('"+new String(txtpassword.getPassword())+"')");
        
        while(rs.next()){
            //System.out.println(rs.getString("Username"));
                if(txtuname.getText().equals(rs.getString("Username"))){
                    new MainWindow().setVisible(true);
                    new  User_Details(rs.getString("Position"));
                    this.dispose();
                    return;
                }
        }
      JOptionPane.showMessageDialog(null,"Invalid Username or Password!");
           txtuname.setText("");
           txtpassword.setText("");
           txtuname.grabFocus();
    } catch (SQLException ex) {
           ex.printStackTrace();
        }
         jdbc.close();
    }//GEN-LAST:event_btnLoginActionPerformed
    
