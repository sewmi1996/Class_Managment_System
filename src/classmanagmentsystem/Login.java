    private String genNewPass() {
          String pass="";
        int i=0;
        while(i<10){
            int j=(int)(Math.random()*124);
            if((48<=j && j<=57) || (65<=j && j<=90) || (97<=j && j<=122)){
                pass+=(char)j;
                i++;
           }
        }
        return pass;
    }
