    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        DefaultTableModel d=(DefaultTableModel)tblsubject.getModel();
        int i=-1;
        i=tblsubject.getSelectedRow();
        if(i>=0 && i<d.getRowCount()){
            int ans=JOptionPane.showConfirmDialog(null, "Do you want to delete "+d.getValueAt(i, 0)+" ?");
            if(ans==0){
                new JDBC().putData("DELETE FROM user_details WHERE SubjectID='"+d.getValueAt(i, 0)+"'");
                
                JOptionPane.showMessageDialog(null, "Selected subject removed.");
                loadUsers();
            }
        }else{
            JOptionPane.showMessageDialog(null, "Please select a row");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

