    private void loadUsers() {
        try {
            DefaultTableModel d=(DefaultTableModel) tblsubject.getModel();
            while(d.getRowCount()>0){
                d.removeRow(0);
            }
            ResultSet rs=new JDBC().getData("SELECT * From subject_details");
            
            while(rs.next()){
                Vector v=new Vector();
                v.add(rs.getString("SubjectID"));
                v.add(rs.getString("SubjectName"));
                
                
                d.addRow(v);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

